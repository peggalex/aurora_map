const getEaseOutPercent = ({ percent }: { percent: number }) => {
	return percent ** 0.5;
};

export const getColorFromPercent = ({ percent }: { percent: number }) => {
	const colorIndex = Math.round(
		getEaseOutPercent({ percent }) * (colors.length - 1)
	);
	return `rgb${colors[colorIndex]}`;
};
